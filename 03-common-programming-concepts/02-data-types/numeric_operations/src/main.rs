fn main() {
    // addition
    println!("addition: {}", 5 + 10); // 15

    // subtraction
    println!("subtraction: {}", 95.5 - 4.3); // 91.2

    // multiplication
    println!("multiplication: {}", 4 * 30); // 120

    // division, quotient
    println!("division, quotient: {}", 56.7 / 32.2); // 1.7608695652173911

    // division, floored
    println!("division, floored: {}", 2 / 3); // 0

    // division, not allowed
    // println!("division, not allowed: {}", 2.3 / 3); // cannot divide `{float}` by `{integer}`

    // remainder
    println!("remainder: {}", 43 % 5); // 3
}
